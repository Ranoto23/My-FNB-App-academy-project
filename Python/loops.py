#Loops

#Using a for loop to list

fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)
    
    
    numbers = [1, 2, 3, 5, 7]
    
    for number in numbers:
        print(number)
        
    #Using a while loop to count
    
    count = 1
    
    while count <= 5:
        print(count)
        count += 1 # Increments the count by 1